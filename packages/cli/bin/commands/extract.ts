import { Command } from "commander";
import * as fs from "node:fs";
import * as path from "node:path";
import { glob } from "glob";
import {
  extractContractsFromTypeScript,
  findProjectRoot,
  loadConfig,
} from "@specferret/core";

const CONTRACT_ID_PATTERN = /^[A-Za-z0-9_.\/-]+$/;

function normalizeFileName(input: string): string {
  return input
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function mapIdToContractPath(specDir: string, id: string): string {
  const [namespace, ...rest] = id.split(".");
  const name = rest.join(".") || "contract";
  const fileName = `${normalizeFileName(name)}.contract.md`;
  return path.join(specDir, namespace, fileName);
}

function parseFrontmatterId(content: string): string | null {
  const match = content.match(/^\s*id:\s*([^\n\r]+)\s*$/m);
  return match ? match[1].trim() : null;
}

function normalizeSchema(value: unknown): unknown {
  if (Array.isArray(value)) {
    return value.map((v) => normalizeSchema(v));
  }

  if (!isRecord(value)) {
    return value;
  }

  const out: Record<string, unknown> = {};
  for (const key of Object.keys(value).sort()) {
    const normalizedValue = normalizeSchema(value[key]);
    if (key === "required" && Array.isArray(normalizedValue)) {
      const asStrings = normalizedValue.map((v) => String(v));
      out[key] = [...new Set(asStrings)].sort();
      continue;
    }
    out[key] = normalizedValue;
  }
  return out;
}

function isRecord(value: unknown): value is Record<string, unknown> {
  return typeof value === "object" && value !== null && !Array.isArray(value);
}

function toYaml(value: unknown, indent = 0): string {
  const pad = " ".repeat(indent);

  if (Array.isArray(value)) {
    if (value.length === 0) return "[]";
    return value
      .map((item) => {
        if (isRecord(item) || Array.isArray(item)) {
          return `${pad}-\n${toYaml(item, indent + 2)}`;
        }
        return `${pad}- ${String(item)}`;
      })
      .join("\n");
  }

  if (isRecord(value)) {
    const keys = Object.keys(value).sort();
    if (keys.length === 0) return "{}";
    return keys
      .map((key) => {
        const item = value[key];
        if (isRecord(item) || Array.isArray(item)) {
          return `${pad}${key}:\n${toYaml(item, indent + 2)}`;
        }
        return `${pad}${key}: ${String(item)}`;
      })
      .join("\n");
  }

  return `${pad}${String(value)}`;
}

function renderContractMarkdown(
  id: string,
  type: string,
  shape: Record<string, unknown>,
  sourceFile: string,
  sourceSymbol: string,
): string {
  return `---\nferret:\n  id: ${id}\n  type: ${type}\n  shape:\n${toYaml(shape, 4)}\n---\n\n# ${id}\n\nGenerated by ferret extract from ${sourceFile} (${sourceSymbol}).\n`;
}

export const extractCommand = new Command("extract")
  .description(
    "Deterministically scaffold .contract.md files from exported TypeScript declarations (annotation overrides supported).",
  )
  .option("--write", "Write generated contracts to disk (default: true)", true)
  .action(async () => {
    const root = findProjectRoot();
    const config = loadConfig();

    const includes = config.codeContracts?.include ?? ["src/**/*.ts"];
    const files = new Set<string>();

    for (const pattern of includes) {
      const matches = await glob(pattern, { cwd: root, absolute: false });
      for (const m of matches) {
        files.add(m.replace(/\\/g, "/"));
      }
    }

    const diagnostics: string[] = [];
    let created = 0;
    let updated = 0;
    let skipped = 0;
    let failed = 0;
    const pathToContractId = new Map<string, string>();

    for (const relFile of [...files].sort()) {
      const absFile = path.join(root, relFile);
      const source = fs.readFileSync(absFile, "utf-8");
      const result = extractContractsFromTypeScript(relFile, source);
      // Hard errors (broken annotations, parse failures) count as failures.
      for (const e of result.errors) {
        diagnostics.push(e);
        failed++;
      }
      // Soft diagnostics (union type fallbacks etc.) are collected as warnings only.
      for (const d of result.diagnostics) {
        diagnostics.push(d);
      }

      for (const contract of result.contracts) {
        if (
          !CONTRACT_ID_PATTERN.test(contract.id) ||
          !contract.id.includes(".")
        ) {
          diagnostics.push(
            `${relFile} (${contract.sourceSymbol}): Invalid contract id '${contract.id}'. ` +
              "Use <namespace>.<name> with only letters, numbers, _, -, /, and .",
          );
          failed++;
          continue;
        }

        const relContractPath = mapIdToContractPath(
          config.specDir,
          contract.id,
        );
        const absContractPath = path.join(root, relContractPath);

        const existingMapped = pathToContractId.get(relContractPath);
        if (existingMapped && existingMapped !== contract.id) {
          diagnostics.push(
            `${relFile} (${contract.sourceSymbol}): Path collision. ` +
              `'${contract.id}' and '${existingMapped}' both map to '${relContractPath}'. ` +
              "Rename contract IDs to produce unique output paths.",
          );
          failed++;
          continue;
        }
        pathToContractId.set(relContractPath, contract.id);

        fs.mkdirSync(path.dirname(absContractPath), { recursive: true });

        const normalizedShape = normalizeSchema(contract.shape) as Record<
          string,
          unknown
        >;

        const content = renderContractMarkdown(
          contract.id,
          contract.type,
          normalizedShape,
          relFile,
          contract.sourceSymbol,
        );

        if (!fs.existsSync(absContractPath)) {
          fs.writeFileSync(absContractPath, content, "utf-8");
          created++;
          continue;
        }

        const existing = fs.readFileSync(absContractPath, "utf-8");
        const existingId = parseFrontmatterId(existing);
        if (existingId && existingId !== contract.id) {
          diagnostics.push(
            `${relFile} (${contract.sourceSymbol}): Output path '${relContractPath}' is already used by '${existingId}'. ` +
              `Cannot safely update with '${contract.id}'.`,
          );
          failed++;
          continue;
        }

        if (existing === content) {
          skipped++;
          continue;
        }

        fs.writeFileSync(absContractPath, content, "utf-8");
        updated++;
      }
    }

    process.stdout.write(
      `ferret extract  created=${created}  updated=${updated}  skipped=${skipped}  failed=${failed}\n`,
    );

    if (failed > 0) {
      for (const d of diagnostics) {
        process.stderr.write(`⚠ ${d}\n`);
      }
      process.exit(1);
      return;
    }

    process.exit(0);
  });
